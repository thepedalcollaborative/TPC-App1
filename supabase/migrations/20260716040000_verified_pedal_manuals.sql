time. In MISO or stereo modes, the LFO is oﬀset on the left and right to create a variety of 
stereo eﬀects. There are three controls over the LFO : Rate, Depth, and Shape.
Rate sets the speed of the LFO. Set all the way down, the movement of the LFO is deactivated. 
Set all the way up, it enters ring mod territory. In envelope modes, Rate sets the sensitivity. 
Note: when LFO movement is deactivated in MISO mode, the left side will feed into the right 
side to create a ping-pong eﬀect.
