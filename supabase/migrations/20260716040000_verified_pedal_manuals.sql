Thermae™ allows all of its parameters to be controlled via control change messages, as well as it’s presets to be saved and recalled with 
program change messages.  
To use MIDI with your Thermae, you’ll have to do the following:
1. Using a standard ¼” TRS patch cable, connect a Chase Bliss Midibox port to the TAP/MIDI jack on your Thermae. The Midibox is not 
included with the Thermae. If you do not have a modified Empress Midibox, but a standard one from Empress, you need to use a custom 
TRS cable that flips the tip and the ring. See chaseblissaudio.com to purchase the items described here.  
2. Thermae is set to MIDI channel 2 by default, but it is easy to change if you need to. If you want to change it, simply hold down both 
stomp switches when you provide power to the pedal (hold down TAP and BYPASS switches simultaneously) and then you can let go. The 
pedal is now looking for the first “Program Change” message it sees, and it will set itself to whatever channel it gets that first message from, 
permanently (until you change it again).
MIDI CONFIGURATION
Recalling a preset via MIDI
You can activate a preset by sending a MIDI Program Change message. 
For example, sending a Program Change message of 1 activates preset 
1. There are 122 total preset slots available. Slots 1 and 2 are the 
presets that can be saved and activated directly from the pedal via the 
preset toggle. Slot 1 is the right slot. Slot 2 is the left slot. Sending a 
Program Change message of 0 puts the pedal in “Live” mode which 
is identical to when the preset toggle switch is in the middle position, 
in normal pedal operation. If you do not save something into presets 
3 through 122, nothing will be recalled. There are no factory presets 
besides the two that come loaded in slots 1 and 2. The idea is for you 
to create your own. 
Saving a preset via MIDI
You save a preset by sending a MIDI Program Change message while 
holding down both the TAP and BYPASS stomp switches. For example, 
sending a Program Change message of 45 while holding down both 
the TAP and BYPASS stomp switches will save your current settings 
to preset 45. There are 122 total slots. Slots 1 and 2 are the presets 
that can be saved and activated directly from the pedal via the preset 
toggle. Slot 1 is the right slot. Slot 2 is the left slot.  
