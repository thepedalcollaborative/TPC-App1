Loop Banks and Slots 
There are two banks of 8 slots for a total of 16 loop slots 
available. They are identified by blinking LED’s on the modifier 
buttons. Bank 1 is blue and bank two is red. You can identify 
the loop number you are on by counting the blinking lights on 
the modifier LED’s. For example, if a slot has blue LED’s and 
blinks four times, that is “slot four.” If a slot has red LED’s and 
blinks three times, that is “slot 11.”
Saving a Loop via MIDI
Blooper is a “zero-based” MIDI pedal. Presets 1-16 are saved 
and recalled using Program Changes 0-15. While in loop mode 
(center toggle position) you save a loop by sending a MIDI 
Program Change message while holding down the right stomp 
switch. For example, sending a Program Change message of 
“5” while holding down the right stomp switch will save your 
current loop to slot #6. A new feature on this Chase Bliss 
pedal, all 16 loop slots can be saved and recalled with just the 
pedal, alone. 
Recalling a loop via MIDI
Blooper is a “zero-based” MIDI pedal. Presets 1-16 are saved 
and recalled using Program Changes 0-15. To use a MIDI 
controller to recall a loop while in loop mode, simply send the 
corresponding Program Change number. PC#0-PC#15. For 
example, sending a Program Change message of “0” loads 
loop 1. 
