Bathing is what we call a liminal delay. It’s what we used to call a phase repeater. It is an 
algorithmic exploit - an underlying structure of reverb, teased apart into something that can 
turn into a phaser or multi-tap delay, and ultimately sounds unlike anything else. It is for explo-
ration; knowing the controls is helpful, but not always necessary. Just save that preset and 
don’t concern yourself with how you got there. Bathe in the wonder of unknown repetitions.
CONTROLS
The main sound of bathing is determined by a delay line divided into many stages called 
all-pass /f_ilters.
Time sets the central delay time.
Alternatively, Time sets the delay subdivisions by pressing Tap/Aux while turning the knob. 
There are six subdivisions, from left to right: sixteenth, dotted sixteenth, eighth, triplet, dotted 
eighth, quarter. When a tempo is tapped in or set via MIDI clock, the central delay time is set 
according to the subdivision. Subdivisions will be saved per-preset, and in live mode, with the 
factory default being quarter note.
Stages sets the number of all-pass stages the delay runs through, from two to twelve. At very 
short time settings, this increases the intensity of the phase//f_lange eﬀect. At medium or longer 
time settings, it creates more repeats both before and after the central delay time.
Feedback sets the feedback of the delay, taking the output and feeding it back into the input. 
This can create longer trails and increased resonance.
Filter applies a combination of low pass and high pass /f_iltering to the output of the delay, as 
well as the delay feedback loop, with the darkest sound to the left and the brightest sound to 
the right.
The LFO in Bathing aﬀects the character of the delay repeats. When time is very short, it acts 
