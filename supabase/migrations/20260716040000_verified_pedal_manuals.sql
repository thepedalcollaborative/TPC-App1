CXM 1978™ allows all of its parameters to be controlled via control change 
messages, as well as its presets to be saved with control change messages, 
and recalled with program change messages.
To connect your CXM 1978™ to a MIDI controller, all you need to do is run a 
standard 5-pin MIDI cable from the “MIDI OUT” port on your MIDI controller to 
the “MIDI IN” port on the pedal. For your convenience, we have also included 
a “MIDI THRU” port allowing MIDI messages coming into the “MIDI IN” port to 
be passed downstream to other MIDI pedals. 
MIDI CHANNEL
CXM 1978™ is set to MIDI channel 2 by default. This can be changed by 
holding down both stomp switches simultaneously when you provide power 
to the pedal and releasing the stomp switches once the seven segment 
display on the front of the pedal lights up. The pedal is now looking for the 
first program change message it sees and will set itself to whichever channel 
it receives that message from. Note: you may need to send that program 
change message more than one time. This is saved as the new MIDI channel 
until you decide to change it again.
SAVING A PRESET VIA MIDI
You can save your current settings via MIDI to any of the 30 preset slots. 
Send CC#27 and the value (0-29) will save the current configuration to the 
intended preset slot. Remember, you can also save a preset to the current 
slot at any time by pressing and holding the SAVE stomp switch on the pedal. 
RECALLING A PRESET VIA MIDI
Presets 0-29 are recalled using program changes 0-29. You can do this by 
sending the corresponding program change # from your MIDI controller. For 
example, sending a program change message of “4” loads bank one (left LED 
off), preset four. Sending a message of “17” loads bank two (left LED red), 
preset seven. Sending a program change of “20” loads bank three (left LED 
green), preset zero.
CONTROL CHANGE MESSAGES
CXM 1978™ can be controlled with MIDI control change messages. View 
the table shown in the upper left that outlines which MIDI control change 
message controls each CXM 1978™ parameter. 
AUX CONTROL
To control the AUX functions on your CXM 1978™ you can plug in a Meris 
Preset Switch with a TRS cable to access two modes: Preset mode and 
Performance mode. Switch between modes by holding the Jump arcade 
button while connecting your TRS cable to the Aux port.
Preset mode is simple, the four switches on the Preset Switch will recall 
presets 1 - 4 on each of the three banks on CXM.
Performance mode has more to it. Switches 1 and 2 on the Preset Switch 
allow you to recall the heel and toe positions, respectively, on any given 
preset. This could also allow you to effectively have 3 presets for any 
dedicated preset slot. Heel and toe positions are set in the expression menu. 
Press switch 1 to access the heel position. Press again to go back to your 
standard preset position. Press switch 2 to access the toe position. Press 
again to go back to your standard preset position.
Switches 3 and 4 are really fun and let you manipulate the reverb buffer. 
Switch 3 instantly kills the reverb tail. This is especially useful for dramatic, 
abrupt terminations of huge reverb trails. Switch 4 acts as a kind of sustain 
locking mechanism, blocking your incoming dry signal from entering the 
reverb path but maxing the reverb tails, allowing you to play over a familiar 
(yet evolving and recirculating) reverb landscape. Press switch 4 again to 
gracefully clear the buffer, or abruptly clear the buffer by pressing switch 3.
