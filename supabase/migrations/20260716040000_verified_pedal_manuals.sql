Chance allows random bits of signal to enter the delay/reverb buﬀer during an LFO cycle. Up 
to noon, the chance of this happening increases from 0-100%, and within that, there is always a 
50-50 chance whether the signal let in will be new (input signal) or old (last output signal). Past 
noon, the new input signal is always allowed in, and the chance of feeding back the old signal 
goes from 0-100%. At maximum, this creates something like a chaotic sound-on-sound looper. 
For constant delay/reverb sounds without surprise feedback, set this knob to noon. To see 
when a chance moment may occur, look at the LED above Aux, which will always pulse at the 
LFO rate.
- Note: when Shape is set to envelope, Chance becomes a sensitivity control for the enve-
lope. In this mode, signal strength (how loud your input is) determines when new signal is 
let into the delay/reverb buﬀer. Higher sensitivity settings will increase signal strength.
