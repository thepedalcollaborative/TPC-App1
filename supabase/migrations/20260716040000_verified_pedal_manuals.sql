There are three “trim pots” (also called trimmers) 
on Brothers AM’s circuit board that let you adjust 
the treble booster to taste. These adjustments are 
NOT required. We believe the default settings are 
just right for most people.
   1   INPUT IMPEDANCE FOR TREBLE BOOSTER 
The treble booster included in Brothers AM is a Rangemaster-style 
circuit with a low input impedance. It’s happiest when connected 
directly to a guitar (no buffers). This trimmer lets you introduce some 
input impedance so that the circuit behaves much better after a 
buffer. The default setting is max CW (no additional impedance). To 
introduce some impedance, simply move this trimmer 
counter-clockwise. A good starting point after a typical buffer is noon 
(straight up and down), but feel free to experiment.
  2   OUTPUT FOR TREBLE BOOSTER 
This sets the level of the treble booster. Default is noon, and this is the 
setting that we prefer. It gives the normal KOT circuits a nice little 
bump. If you want the treble booster to hit those circuits harder, rotate 
the trimmer clockwise. If you want less and more of a "unity" type of 
feel, move it counter-clockwise.
  3   BIAS FOR TREBLE BOOSTER 
The default setting here is noon. This biases the transistor in a way 
that is consistent with the typical break-up characteristics of 
Rangemasters. You can squeeze more headroom out of this circuit 
and give it more of a symmetrical break-up by experimenting with the 
position of this trimmer. There are no specific rules here, try out some 
different settings and see if you have a preference.
