The Aux footswitch has three primary functions. It can be tapped to latch, or held for momentary action.
- Hold creates nearly in/f_inite sustain of the reverb trail when engaged, maxing out Decay beyond the knob setting 
and stopping any new input from getting in. When disengaged, Decay returns to the previous setting.
- Filter closes the low pass /f_ilter when engaged, and returns to the previous setting when disengaged.
- Pitch sets both pitch shifts to +1 octave when engaged, and returns to the previous setting when disengaged.
To set the Aux function, hold the Aux footswitch and move the knob related to the setting (Decay, Filter, or Pitch 1). 
When the switch is let go, if a knob was moved, the setting will be saved. This setting is saved per-preset. The Aux 
footswitch can also be used to cycle through the on-board presets. To engage this mode, tap the Preset switch 
while holding Aux. To return to the previously assigned setting, tap Preset again while holding Aux.
The ON/OFF footswitch turns the pedal on and oﬀ. Tap to latch, hold for momentary action.
The Expression Jack can be used to externally control any combination of knobs on Dark Star, using a TRS expres-
sion device. To assign the Expression control, simply turn a knob or knobs while holding the On/Oﬀ footswitch. The 
knob position when you press the footswitch will be the expression heel position, and the knob position when you 
let go will be the expression toe position. If a knob wasn’t moved, expression will not aﬀect it. Expression settings 
are saved per-preset.
Presets are accessible via the Preset switch. Press the switch to cycle through stored presets. The LEDs will 
indicate whether you are on Preset 1 (top LED), Preset 2 (middle), Preset 3 (bottom), or live mode (no LED is lit). To 
save a preset, hold the Preset switch for /f_ive seconds until the preset LED starts blinking. Cycle through the presets 
using the switch to choose which slot to save your preset to. If you do not want to save, cycle to live mode (no LED 
lit). Once you’ve made your selection, hold the Preset switch for /f_ive seconds again until the LED stops blinking.
The Routing of Dark Star can be Mono, Stereo, or Mono-In-Stereo-Out. To choose routing, hold down the On/oﬀ 
switch and press the Preset switch to cycle through options. The preset LEDs will blink indicating the current 
setting: Mono (top LED), Mono In Stereo Out (middle LED), or Stereo (bottom LED).
Additionally, Dark Star can be true bypass or have trails. To set this, hold down the Preset switch for /f_ive seconds 
while assigning stereo/mono routing. A faster blinking LED indicates trails are on, a slower blink indicates trails are 
oﬀ. The routing and trails settings will save globally.
To Factory Reset your Dark Star, hold ON/OFF and Aux for 10 seconds until all LEDs begin blinking. At this point, 
you can cancel the reset by pressing ON/OFF. To con/f_irm the reset, press Aux. All LEDs will light, then count 
upward as all settings and presets are returned to default, then light once more, then return to normal operation. 
This will delete all user settings and set all presets to their factory state.
