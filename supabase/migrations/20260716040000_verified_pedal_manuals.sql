Filter adds either a low pass /f_ilter or high pass /f_ilter to the reverb signal. At noon, it gently /f_ilters highs similar to the 
original Dark Star response. Below noon, the low pass /f_ilter cutoﬀ decreases until no signal is let through. Above 
noon, the high pass /f_ilter cutoﬀ increases to cut lows and low mids.
Pitch 1 adds a pitch shift to the Left channel. At noon, the pitch shift is bypassed for unity pitch. Near noon, it gives 
a slight detune up or down. Further away from center, it snaps in semitones down one octave or up one octave, with 
additional two octave settings at the extremes.
Pitch 2 adds a pitch shift to the Right channel. At noon, the pitch shift is bypassed for unity pitch. Near noon, it gives 
a slight detune up or down. Further away from center, it snaps in semitones down one octave or up one octave, with 
additional two octave settings at the extremes. Note: in mono mode, if this pitch shift isn’t bypassed by the knob 
being at noon, it is added in parallel to the Left signal’s pitch shift, like the original Dark Star’s Pitch mode.
Crush creates distortion in the reverb. At noon, signal is clean. Below noon, sample rate is turned down to add 
distortion and aliasing, like Crush mode in the original Dark Star. Above noon, the reverb is passed through a soft 
clipping overdrive with increasing gain as it’s turned.
Lag sets the predelay time of the reverb, allowing the trail to start well after the note is played.
Decay sets the decay time of the reverb, from very short to nearly in/f_inite.
Multiply adds feedback to the whole system, increasing the intensity of every eﬀect and allowing for self-oscillation 
in some settings.
Spread sets the stereo spread/left-right blend of the reverb signals. All the way up, signals are as expected, with left 
reverb on left side and right reverb on right. In the center, both signals are sent to both outputs equally. All the way 
down, signals are fully reversed, with left reverb on the right out and vice versa. 
Volume sets the overall volume.
Mix sets the wet/dry mix, from 100% dry to 100% wet, with roughly unity blend in the center. Dark Star is analog dry 
through, meaning the dry signal is never digitized and has zero latency.
